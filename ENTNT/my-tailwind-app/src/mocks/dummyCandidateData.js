// Helper function to search candidates by name or skills
export const searchCandidates = (query) => {
  const lowercaseQuery = query.toLowerCase();
  return dummyCandidates.filter(candidate => 
    candidate.name.toLowerCase().includes(lowercaseQuery) ||
    candidate.skills.some(skill => skill.toLowerCase().includes(lowercaseQuery))
  );
};

